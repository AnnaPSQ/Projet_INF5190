CREATE TABLE piscines(
    id integer primary key,
    id_uev int,
    type varchar(256),
    nom varchar(256),
    nom_arrondissement varchar(256),
    adresse varchar(256),
    propriete varchar(256),
    gestion varchar(256),
    point_x varchar(256),
    point_y varchar(256),
    equipement varchar(256),
    long varchar(256),
    lat varchar(256)
);